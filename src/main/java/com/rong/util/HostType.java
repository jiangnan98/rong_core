package com.rong.util;

public class HostType {
    private String type;
    private String ip;

    public HostType(String type) {
        this.type = type;
    }

    public String getStrType() {
        return type;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getIp() {
        return ip;
    }

}